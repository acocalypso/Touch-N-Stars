export function getConstellation(raDeg, decDeg) {
  const { ra: raH, dec } = toB1875(raDeg, decDeg);
  for (const [lo, hi, decLim, abbr] of B) {
    if (dec >= decLim && raH >= lo && raH < hi) return abbr;
  }
  return '?';
}

export function getConstellationFull(raDeg, decDeg) {
  const abbr = getConstellation(raDeg, decDeg);
  return CON_NAMES[abbr] ?? abbr;
}
