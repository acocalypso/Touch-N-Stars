// Precess J2000 RA/Dec to B1875 (simplified Newcomb general precession)
function toB1875(raDeg, decDeg) {
  const ra = (raDeg * Math.PI) / 180;
  const dec = (decDeg * Math.PI) / 180;
  const T = -125; // years (J2000→B1875)
  const dRA = ((3.07327 + 1.33617 * Math.sin(ra) * Math.tan(dec)) * T) / 240; // deg
  const dDec = (20.0468 * Math.cos(ra) * T) / 3600; // deg
  return {
    ra: ((((raDeg + dRA) % 360) + 360) % 360) / 15, // convert to hours
    dec: decDeg + dDec,
  };
}

